    private void tbNhanVienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbNhanVienMouseClicked

        int selectedRowIndex = tbNhanVien.getSelectedRow();
        selectedRowIndex = tbNhanVien.convertRowIndexToModel(selectedRowIndex);

        nhanVien.setMANV( dtmnv.getValueAt(selectedRowIndex, 0)+"");
        nhanVien.setHoTen(tbNhanVien.getValueAt(selectedRowIndex, 1)+"");
        nhanVien.setNGSINH( tbNhanVien.getValueAt(selectedRowIndex, 2)+"");
        nhanVien.setGIOITINH(tbNhanVien.getValueAt(selectedRowIndex, 3).toString().equalsIgnoreCase("Nam"));
        nhanVien.setDiaChi(tbNhanVien.getValueAt(selectedRowIndex, 4)+"");
        nhanVien.setSODT(tbNhanVien.getValueAt(selectedRowIndex, 5)+"");
        nhanVien.setMAPB(tbNhanVien.getValueAt(selectedRowIndex, 6)+"");
        nhanVien.setMACV(tbNhanVien.getValueAt(selectedRowIndex, 7)+"");
        nhanVien.setTinhTrang(tbNhanVien.getValueAt(selectedRowIndex, 8).toString().equalsIgnoreCase("Còn"));

        NhanVienJframe frame = new NhanVienJframe(nhanVien);
        frame.setTitle("Thông tin học viên");
        frame.setResizable(false);
        frame.setLocationRelativeTo(null);
        frame.setVisible(true);
    }//GEN-LAST:event_tbNhanVienMouseClicked


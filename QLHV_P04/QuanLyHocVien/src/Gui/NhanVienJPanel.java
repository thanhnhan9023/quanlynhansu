public void hienthi()
{
      ArrayList<NhanVien> list = DaoNhanVien.layDachSachNV();
//         loadcombox();
                       dtmnv=(DefaultTableModel) tbNhanVien.getModel();
                       
                        dtmnv.setRowCount(0);
                        for(NhanVien nv:list)
                        {
                            Vector<Object>vec=new Vector<>();
                          vec.add(nv.getMANV());
                          vec.add(nv.getHoTen());
                          vec.add(nv.getNGSINH());
                          vec.add((nv.isGIOITINH())== true ? "Nam" : "Nữ");
                          vec.add(nv.getDiaChi());
                          vec.add(nv.getSODT());
                           vec.add(nv.getMAPB());
                           vec.add((nv.getMACV()));
                           vec.add((nv.isTinhTrang())== true ? "Còn":"Nghĩ");
                           
                            dtmnv.addRow(vec);
                        }
                      
}
